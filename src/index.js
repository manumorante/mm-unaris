/**
 * Add two numbers
 * @param a number one
 * @param b number two
 * @returns result of adding a and b
 * @example add(1, 2) // 3
 */
export const add = (a, b) => a + b
/**
 * Subtract two numbers
 * @param a number one
 * @param b number two
 * @returns result of subtracting b from a
 * @example subtract(5, 3) // 2
 */
export const subtract = (a, b) => a - b
/**
 * Multiply two numbers
 * @param a number one
 * @param b number two
 * @returns result of multiplying a and b
 * @example multiply(2, 3) // 6
 */
export const multiply = (a, b) => a * b
